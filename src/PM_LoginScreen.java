    //method to remove trailing whitespace
    public String removeTrailSpace(String s) {
        int len = s.length();
        for(;len>0;len--) {
            if(!Character.isWhitespace(s.charAt(len-1))) break;
        }
        return s.substring(0,len);      
    }
    
    
    //method to remove trailing space and create file name
    public String tfileName(String u, String p) {
        int len = u.length();
        for(;len>0;len--) {
            if(!Character.isWhitespace(u.charAt(len-1))) break;
        }
        u = u.substring(0,len);
        return u+ "-" + p + ".txt";
    }
