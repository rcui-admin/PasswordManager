    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        String userName = loginUsernameTextField.getText(); 
        String password = loginPasswordTextField.getText();
        ArrayList<String> fileList = new ArrayList<>();
        
        textFileName = tfileName(userName,password);
        
        File folder = new File("src/userAccounts/");
        
        File[] listofFiles = folder.listFiles();
        for(File file : listofFiles) {
            if(file.isFile()) {
                fileList.add(file.getName());
            }
        }
        
        if(fileList.contains(textFileName)) {
            // save to text file of last login user credentials
            try {
                this.fw = new FileWriter("src/lastUserLogin.txt",true);
                this.pw = new PrintWriter(this.fw);
                this.pw.println(textFileName);
                this.pw.close();
            }
            catch(IOException ex) {
                Logger.getLogger(PM_MainScreen.class.getName()).log(Level.SEVERE,null,ex);
            }
            //FileException not used here as file is already caught
            JOptionPane.showMessageDialog(this, "Login Successful!");
            this.setVisible(false);
            new PM_MainScreen().setVisible(true);
        }
        else {
            JOptionPane.showMessageDialog(this, "User Not Found");
            loginUsernameTextField.setText("");
            loginPasswordTextField.setText("");
        }
        // code to check if username & password matches any file in database
    }//GEN-LAST:event_loginButtonActionPerformed

