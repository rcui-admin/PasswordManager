    private void createAccountButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createAccountButtonActionPerformed
        String userName = loginUsernameTextField.getText();
        String password = loginPasswordTextField.getText();
        
        //remove trailing spaces from username
        userName = removeTrailSpace(userName);
        
        //if-else statement to read text fields and more
        if(userName.length()>0 && password.length()>0) {   
            try {
                String fileName= userName + "-" + password + ".txt";
                String dirName = "src/userAccounts/";
                File dir = new File(dirName);
                File userFile = new File(dir,fileName);
                if(userFile.createNewFile()) {
                    JOptionPane.showMessageDialog(this, "File Created:  " + fileName + "\nWelcome, New User!");
                    this.setVisible(false);
                    new PM_MainScreen().pack();
                    new PM_MainScreen().setLocationRelativeTo(null);
                    new PM_MainScreen().setVisible(true);
                }
                else {
                    JOptionPane.showMessageDialog(this, "User Already Exists!");
                    loginUsernameTextField.setText("");
                    loginPasswordTextField.setText("");
                }                             
            }
            catch(IOException ex) {
                Logger.getLogger(PM_MainScreen.class.getName()).log(Level.SEVERE,null,ex);
            }
        }
        else {
            JOptionPane.showMessageDialog(this, "Enter a valid username and password.");
        }
    }//GEN-LAST:event_createAccountButtonActionPerformed

